package com.kovizone.admin.po;

/**
 * 系统角色权限关联PO
 *
 * @author KoviChen
 * @version 0.0.1 2019-08-08 KoviChen 新建类
 */
public class SystemRolePermission {

	private Integer rpno;
	private Integer rno;
	private Integer pno;

	public Integer getRpno() {
		return rpno;
	}

	public void setRpno(Integer rpno) {
		this.rpno = rpno;
	}

	public Integer getRno() {
		return rno;
	}

	public void setRno(Integer rno) {
		this.rno = rno;
	}

	public Integer getPno() {
		return pno;
	}

	public void setPno(Integer pno) {
		this.pno = pno;
	}
}
